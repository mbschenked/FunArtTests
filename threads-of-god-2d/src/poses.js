// poses.js — pure data + sampling math for the articulated vector swordsmen.
// No imports. WP-D implements.
//
// A Pose is a joint-angle object (radians, facing-local — render mirrors by
// facing). Origin is the fighter's feet; rootY lifts the pelvis.
//
// Pose = {
//   rootY,            // pelvis height offset (negative = crouch)
//   lean,             // whole-torso lean angle
//   torso, head,      // segment angles
//   armF: [shoulder, elbow],   // sword arm (front)
//   armB: [shoulder, elbow],   // off arm (back)
//   legF: [hip, knee],
//   legB: [hip, knee],
//   sword,            // sword angle relative to front forearm
// }
//
// AUTHORING CONVENTION (frozen — all attack tracks must follow):
// - A track is an array of keyframes {t, pose} with t in [0,1] spanning the
//   FULL attack (windup + active + recovery).
// - The contact-extreme keyframe sits exactly at t = windup/(windup+active+recovery)
//   so the visual hit lines up with the hitbox arming frame.
// - Every attack track needs >= 4 keys: anticipation, contact, follow-through, settle.
// - Looping tracks (idle, walk, chargeLoop) interpolate last->first.

