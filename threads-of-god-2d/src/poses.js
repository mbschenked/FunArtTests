// ─── field layout in the Pose object ─────────────────────────────────────────
// Used by lerpPose to iterate all numeric fields uniformly.
const SCALAR_FIELDS = ['rootY', 'lean', 'torso', 'head', 'sword'];
const ARRAY_FIELDS  = ['armF', 'armB', 'legF', 'legB'];

function lerpPose(a, b, t) {
  const out = {};
  for (const k of SCALAR_FIELDS) out[k] = a[k] + (b[k] - a[k]) * t;
  for (const k of ARRAY_FIELDS) {
    out[k] = a[k].map((v, i) => v + (b[k][i] - v) * t);
  }
  return out;
}

/**
 * Piecewise-lerp every numeric field (and array elements) between the two
 * keyframes bracketing t01. Clamps outside [0,1]. Loops for loop tracks.
 * @param {string} trackName key of POSES (falls back to 'idle' if missing)
 * @param {number} t01 normalized time
 * @param {boolean} [loop=false]
 * @returns {object} a complete Pose
 */
export function samplePose(trackName, t01, loop = false) {
  const track = POSES[trackName] ?? POSES['idle'];
  const len = track.length;

  if (loop) {
    // Wrap t01 into [0, 1) then interpolate last→first across the seam
    t01 = t01 - Math.floor(t01);
    if (t01 < 0) t01 += 1;

    // Find the segment
    for (let i = 0; i < len - 1; i++) {
      const kA = track[i], kB = track[i + 1];
      if (t01 >= kA.t && t01 <= kB.t) {
        const span = kB.t - kA.t;
        const local = span > 0 ? (t01 - kA.t) / span : 0;
        return lerpPose(kA.pose, kB.pose, local);
      }
    }
    // Between last and first (wrap seam)
    const kLast = track[len - 1];
    const kFirst = track[0];
    const lastT = kLast.t;
    const span = 1 - lastT;
    if (span > 0) {
      const local = (t01 - lastT) / span;
      return lerpPose(kLast.pose, kFirst.pose, local < 0 ? local + 1 : local);
    }
    return { ...kFirst.pose };
  }

  // Non-looping: clamp to [0, 1]
  const tc = t01 < 0 ? 0 : t01 > 1 ? 1 : t01;

  if (tc <= track[0].t) return { ...track[0].pose, armF: [...track[0].pose.armF], armB: [...track[0].pose.armB], legF: [...track[0].pose.legF], legB: [...track[0].pose.legB] };
  if (tc >= track[len - 1].t) {
    const p = track[len - 1].pose;
    return { ...p, armF: [...p.armF], armB: [...p.armB], legF: [...p.legF], legB: [...p.legB] };
  }

  for (let i = 0; i < len - 1; i++) {
    const kA = track[i], kB = track[i + 1];
    if (tc >= kA.t && tc <= kB.t) {
      const span = kB.t - kA.t;
      const local = span > 0 ? (tc - kA.t) / span : 0;
      return lerpPose(kA.pose, kB.pose, local);
    }
  }

  // Fallback: return last keyframe
  const p = track[len - 1].pose;
  return { ...p, armF: [...p.armF], armB: [...p.armB], legF: [...p.legF], legB: [...p.legB] };
}
